void Map::Initialize(Player* _player)
{
	this->player_ = _player;
}

void Map::Update()
{
	
	for (int y = 0; y < length; y++)
	{
		for (int x = 0; x < width; x++)
		{
			if (mapStageCount == 1)
			{
				player_->SetMaxWidth(3560);
				if (mapStage1[y][x] == 1)
				{
					mapData[y][x] = 1;
				}
				if (mapStage1[y][x] == 2)
				{
					mapData[y][x] = 2;
				}
			}

			if (mapStageCount == 2)
			{
				player_->SetMaxWidth(4160);
				if (mapStage2[y][x] == 1)
				{
					mapData[y][x] = 1;
				}
				if (mapStage2[y][x] == 2)
				{
					mapData[y][x] = 2;
				}
			}

			if (mapStageCount == 3)
			{
				player_->SetMaxWidth(4760);
				if (mapStage2[y][x] == 1)
				{
					mapData[y][x] = 1;
				}
				if (mapStage2[y][x] == 2)
				{
					mapData[y][x] = 2;
				}
			}
		}
	}
}

void Map::Draw()
{
	for (int y = 0; y < length; y++)
	{
		for (int x = 0; x < width; x++)
		{
			if(mapData[y][x] == 1)
			{
				DrawRectGraph(x * 64 - player_->GetScrollX(), y * 64, 0, 0, 64, 64, blockGraph, TRUE, FALSE);
			}
			if (mapData[y][x] == 2)
			{
				DrawBox(
					x * 64 - player_->GetScrollX(),
					y * 64,
					(x + 1) * 64 - player_->GetScrollX(),
					(y + 1) * 64,
					GetColor(0, 255, 0),
					true
				);
			}
		}
	}
}