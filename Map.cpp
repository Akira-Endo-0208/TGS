void Map::Initialize(Player* _player, OtherScene* _otherScene)
{
	this->player_ = _player;
	this->otherScene_ = _otherScene;
}

void Map::Update()
{
	mapStageCount = otherScene_->GetSelectScene();

	for (int y = 0; y < length; y++)
	{
		for (int x = 0; x < width; x++)
		{
			if (mapStageCount == 4)
			{
				player_->SetMaxWidth(3200);
				if (tutorial[y][x] == 1)
				{
					mapData[y][x] = 1;
				}
				if (tutorial[y][x] == 2)
				{
					mapData[y][x] = 2;
				}
			}

			if (mapStageCount == 5)
			{
				player_->SetMaxWidth(2560);
				if (mapStage1[y][x] == 1)
				{
					mapData[y][x] = 1;
				}
				if (mapStage1[y][x] == 2)
				{
					mapData[y][x] = 2;
				}
			}

			if (mapStageCount == 6)
			{
				player_->SetMaxWidth(3200);
				if (mapStage2[y][x] == 1)
				{
					mapData[y][x] = 1;
				}
				if (mapStage2[y][x] == 2)
				{
					mapData[y][x] = 2;
				}
			}

			if (mapStageCount == 7)
			{
				player_->SetMaxWidth(3840);
				if (mapStage3[y][x] == 1)
				{
					mapData[y][x] = 1;
				}
				if (mapStage3[y][x] == 2)
				{
					mapData[y][x] = 2;
				}
			}
		}
	}
}

void Map::Draw()
{
	if (mapStageCount > 4)
	{
		DrawRectGraph(10, 40, 0, 0, 36 * player_->GetLife(), 32, lifeGraph, TRUE, FALSE);
	}

	for (int y = 0; y < length; y++)
	{
		for (int x = 0; x < width; x++)
		{
			if(mapData[y][x] == 1)
			{
				DrawRectGraph(x * 64 - player_->GetScrollX(), y * 64, 0, 0, 64, 64, blockGraph, TRUE, FALSE);
			}
			if (mapData[y][x] == 2)
			{
				DrawBox(
					x * 64 - player_->GetScrollX(),
					y * 64,
					(x + 1) * 64 - player_->GetScrollX(),
					(y + 1) * 64,
					GetColor(0, 255, 0),
					true
				);
			}
		}
	}
}