void Map::Update()
{
	
	for (int y = 0; y < length; y++)
	{
		for (int x = 0; x < width; x++)
		{
			if (mapStageCount == 1)
			{
				player_->SetMaxWidth(3560);
				if (mapStage1[y][x] == 1)
				{
					mapData[y][x] = 1;
				}
				if (mapStage1[y][x] == 2)
				{
					mapData[y][x] = 2;
				}
			}
		}
	}


}

